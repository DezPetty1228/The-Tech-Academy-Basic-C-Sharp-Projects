using System;
using System.Collections.Generic;
using System.Text;

namespace CAlling_Method_Submission_Assignment
{
    class MathOperations
    {
        public static int AddTwo(int number)
        {
            // Adds 2 to the input number and returns the result.
            return number + 2;
        }

        public static int MultiplyByFive(int number)
        {
            // Multiplies the input number by 5 and returns the result.
            return number * 5;
        }

        public static int Square(int number)
        {
            // Squares the input number and returns the result.
            return number * number;
        }
    }
}
